app = FastAPI()

app.add_middleware(
    CORSMiddleware,
    allow_origins=["http://localhost:5173"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

model = joblib.load("dna_mutation_model.pkl")


class MutationInput(BaseModel):
    mutation_type: str
    gene_symbol: str


class UserAuth(BaseModel):
    username: str
    password: str


@app.get("/")
def home():
    return {
        "message": "DNA Mutation Classifier API Running"
    }


@app.post("/predict")
def predict(data: MutationInput):

    features = data.mutation_type + " " + data.gene_symbol

    prediction = model.predict([features])[0]

    probabilities = model.predict_proba([features])[0]

    confidence = max(probabilities) * 100

    return {
        "prediction": prediction,
        "confidence": round(confidence, 2)
    }

@app.post("/download-report")
def download_report(data: MutationInput):

    features = data.mutation_type + " " + data.gene_symbol

    prediction = model.predict([features])[0]

    probabilities = model.predict_proba([features])[0]

    confidence = round(max(probabilities) * 100, 2)

    risk_level = (
        "High"
        if confidence >= 80
        else "Moderate"
        if confidence >= 60
        else "Low"
    )

    gene_info = {
        "BRCA1": ("17", "Breast & Ovarian Cancer"),
        "BRCA2": ("13", "Breast, Ovarian & Prostate Cancer"),
        "TP53": ("17", "Li-Fraumeni Syndrome"),
        "EGFR": ("7", "Lung Cancer"),
        "KRAS": ("12", "Colorectal & Pancreatic Cancer"),
    }

    chromosome, disease = gene_info.get(
        data.gene_symbol,
        ("N/A", "Not Available")
    )

    summary = (
        "Potentially harmful mutation detected."
        if prediction == "Pathogenic"
        else "Mutation appears mostly non-harmful."
    )

    generated_time = datetime.now().strftime(
        "%d %b %Y %I:%M %p"
    )

    pdf_file = "dna_mutation_report.pdf"

    doc = SimpleDocTemplate(pdf_file)

    styles = getSampleStyleSheet()

    content = []

    content.append(
        Paragraph(
            "<font size='24'><b>DNA Mutation Analysis Report</b></font>",
            styles["Title"]
        )
    )

    content.append(Spacer(1, 20))

    details = [
        ["Mutation Type", data.mutation_type],
        ["Gene Symbol", data.gene_symbol],
        ["Prediction", prediction],
        ["Confidence", f"{confidence}%"],
        ["Risk Level", risk_level],
    ]

    table = Table(details, colWidths=[150, 250])

    table.setStyle(
        TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.lightblue),
            ("GRID", (0, 0), (-1, -1), 1, colors.black),
            ("FONTNAME", (0, 0), (-1, -1), "Helvetica-Bold"),
            ("BACKGROUND", (0, 0), (0, -1), colors.whitesmoke),
        ])
    )

    content.append(table)

    content.append(Spacer(1, 25))

    content.append(
        Paragraph(
            "<b>AI Summary</b>",
            styles["Heading2"]
        )
    )

    content.append(
        Paragraph(
            summary,
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 20))

    content.append(
        Paragraph(
            "<b>Gene Information</b>",
            styles["Heading2"]
        )
    )

    content.append(
        Paragraph(
            f"Chromosome: {chromosome}",
            styles["BodyText"]
        )
    )

    content.append(
        Paragraph(
            f"Associated Disease: {disease}",
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 20))

    content.append(
        Paragraph(
            f"Generated On: {generated_time}",
            styles["BodyText"]
        )
    )

    content.append(Spacer(1, 30))

    content.append(
        Paragraph(
            "<font color='grey'>Generated by DNA Mutation Analysis Platform</font>",
            styles["Italic"]
        )
    )

    doc.build(content)

    return FileResponse(
        pdf_file,
        media_type="application/pdf",
        filename="dna_mutation_report.pdf"
    )

